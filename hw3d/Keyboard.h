#pragma once
#include <queue>
#include <bitset>



//键盘类实际上是Window类的一个拓展， 所以键盘把窗口看做友元 允许他为所欲为是有道理的 
class Keyboard
{
	friend class Window;
public:
	class Event
	{
	public:
		enum class Type
		{
			Press,
			Release,
			Invalid
		};
	private:
		Type type;
		unsigned char code;
	public:
		Event() noexcept
			:
			type(Type::Invalid),
			code(0u)
		{}
		Event(Type type, unsigned char code) noexcept
			:
			type(type),
			code(code)
		{}
		bool IsPress() const noexcept
		{
			return type == Type::Press;
		}
		bool IsRelease() const noexcept
		{
			return type == Type::Release;
		}
		bool IsValid() const noexcept
		{
			return type != Type::Invalid;
		}
		unsigned char GetCode() const noexcept
		{
			return code;
		}
	};
public:
	Keyboard() = default; //默认构造函数
	Keyboard(const Keyboard&) = delete;
	Keyboard operator=(const Keyboard&) = delete;
	
	//Key Event stuff
	bool KeyIsPressed(unsigned char keycode) const noexcept; //传入一个键码，返回你是否被按下
	Event ReadKey() noexcept;          //读取"键盘事件队列"中的事件
	bool KeyIsEmpty() const noexcept;  //检查“键盘事件队列”是否为空
	void FlushKey() noexcept;          //清除队列
	
	//Char Event stuff
	char ReadChar() noexcept;
	bool CharIsEmpty() const noexcept;
	void FlushChar() noexcept;
	
	void Flush() noexcept; //相当于FlushKeyandChar()
	
	//autorepeat control  自动复制函数
	void EnableAutorepeat() noexcept;
	void DisableAutorepeat() noexcept;
	bool AutorepeatIsEnabled() const noexcept;
	
private:
	void OnKeyPressed(unsigned char keycode) noexcept;
	void OnKeyReleased(unsigned char keycode) noexcept;
	void OnChar(char character) noexcept;
	void ClearState() noexcept;    

	template<typename T>
	static void TrimBuffer(std::queue<T>& buffer) noexcept;

private:
	static constexpr unsigned int nKeys = 256u; 
	static constexpr unsigned int bufferSize = 16u;
	
	bool autorepeatEnabled = false;
	std::bitset<nKeys> keystates; //存了256位的bool
	std::queue<Event> keybuffer;
	std::queue<char> charbuffer;
};
